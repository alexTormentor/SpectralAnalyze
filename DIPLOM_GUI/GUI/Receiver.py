# Constructor Pattern
class Receiver:
    def __init__(self, type_, diag, diameter, resist, darkness, coef_amp, length, dark_prov, width, size, delta, power):
        self.type = type_
        self.diag = diag
        self.diameter = diameter
        self.resist = resist
        self.darkness = darkness
        self.coef_amp = coef_amp
        self.length = length
        self.darkprov = dark_prov
        self.width = width
        self.size = size
        self.delta = delta
        self.power = power
